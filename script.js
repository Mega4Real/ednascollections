// Preview functionality
function showPreview(dress) {
    const modal = document.getElementById('preview-modal');
    const previewVideo = document.getElementById('preview-video');
    const videoSource = previewVideo.querySelector('source');

    // Set expected video source (based on dress id)
    const expectedVideo = `videos/dress${dress.id}.mp4`;

    // Cleanup any previous fallback image
    const existingFallback = document.getElementById('preview-fallback-image');
    if (existingFallback) {
        existingFallback.remove();
    }

    // Ensure video element is visible
    previewVideo.style.display = '';

    // Update source and load
    videoSource.src = expectedVideo;
    previewVideo.load(); // Reload the video with new source

    // Try to play (may be blocked unless muted)
    previewVideo.play().catch(() => {
        // Autoplay may be blocked, that's okay â€” controls are available
    });

    modal.style.display = 'block';

    // If the video fails to load (404 or other), show the image fallback
    function showFallbackImage() {
        // hide the video element
        previewVideo.style.display = 'none';

        // create or show fallback image
        const img = document.createElement('img');
        img.id = 'preview-fallback-image';
        img.src = dress.image;
        img.alt = `Dress ${dress.id}`;
        img.style.width = '100%';
        img.style.height = 'auto';
        img.style.borderRadius = '4px';

        const container = previewVideo.parentElement;
        container.appendChild(img);
    }

    // Attach an error listener that triggers fallback once
    const onError = () => {
        showFallbackImage();
        // remove the error listener after fallback
        previewVideo.removeEventListener('error', onError);
    };

    previewVideo.addEventListener('error', onError);

    // Close modal when clicking the close button or outside the modal
    const closeBtn = document.querySelector('.close-modal');
    closeBtn.onclick = () => modal.style.display = 'none';
    window.onclick = (event) => {
        if (event.target === modal) {
            modal.style.display = 'none';
        }
    };
}

// Setup filters
function setupFilters() {
    const priceSelect = document.getElementById('price-filter');
    const sizeSelect = document.getElementById('size-filter');
    const clearFiltersButton = document.getElementById('clear-filters');

    // Function to apply filters
    function applyFilters() {
        const selectedPriceRange = priceSelect.value;
        const selectedSize = sizeSelect.value;

        const filteredDresses = dresses.filter(dress => {
            // Price filter
            let priceMatch = true;
            if (selectedPriceRange) {
                const [min, max] = selectedPriceRange.split('-').map(Number);
                priceMatch = dress.price >= min && dress.price <= max;
            }

            // Size filter
            let sizeMatch = true;
            if (selectedSize) {
                sizeMatch = dress.sizes.includes(selectedSize);
            }

            return priceMatch && sizeMatch;
        });

        currentDressArray = filteredDresses; // Update current array
        currentPage = 1; // Reset to first page when filtering
        displayDresses(filteredDresses, currentPage);
    }

    // Event listeners for filters
    priceSelect.addEventListener('change', applyFilters);
    sizeSelect.addEventListener('change', applyFilters);

    // Clear filters
    clearFiltersButton.addEventListener('click', () => {
        priceSelect.value = '';
        sizeSelect.value = '';
        currentDressArray = dresses; // Reset to all dresses
        currentPage = 1; // Reset to first page
        displayDresses(dresses, currentPage); // Show all dresses
    });
}

// Modify displayDresses to accept filtered array
function displayDresses(dressArray = currentDressArray, page = currentPage) {
    const container = document.getElementById('dress-container');
    container.innerHTML = '';

    const startIndex = (page - 1) * dressesPerPage;
    const endIndex = startIndex + dressesPerPage;
    const dressesToShow = dressArray.slice(startIndex, endIndex);

    dressesToShow.forEach(dress => {
        const dressElement = createDressElement(dress);
        container.appendChild(dressElement);
    });

    // Reapply selected state to dress cards
    document.querySelectorAll('.dress-card').forEach(card => {
        const id = parseInt(card.dataset.id);
        if (selectedDresses.has(id)) {
            card.classList.add('selected');
            const selectedSize = selectedDresses.get(id).selectedSize;
            const sizeOption = card.querySelector(`.size-option[data-size="${selectedSize}"]`);
            if (sizeOption) sizeOption.classList.add('selected');
        }
    });

    // Update pagination
    updatePagination(dressArray.length);

    // Scroll to top of the page
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// Update pagination controls
function updatePagination(totalDresses) {
    const paginationContainer = document.getElementById('pagination');
    paginationContainer.innerHTML = '';

    const totalPages = Math.ceil(totalDresses / dressesPerPage);

    if (totalPages <= 1) return; // No pagination needed

    // Previous button
    const prevButton = document.createElement('button');
    prevButton.textContent = 'Previous';
    prevButton.disabled = currentPage === 1;
    prevButton.addEventListener('click', () => {
        if (currentPage > 1) {
            currentPage--;
            displayDresses(currentDressArray);
        }
    });
    paginationContainer.appendChild(prevButton);

    // Page numbers
    const maxVisiblePages = 5;
    let startPage = Math.max(1, currentPage - Math.floor(maxVisiblePages / 2));
    let endPage = Math.min(totalPages, startPage + maxVisiblePages - 1);

    if (endPage - startPage + 1 < maxVisiblePages) {
        startPage = Math.max(1, endPage - maxVisiblePages + 1);
    }

    for (let i = startPage; i <= endPage; i++) {
        const pageButton = document.createElement('button');
        pageButton.textContent = i;
        pageButton.classList.toggle('active', i === currentPage);
    pageButton.addEventListener('click', () => {
        currentPage = i;
        displayDresses(currentDressArray);
    });
        paginationContainer.appendChild(pageButton);
    }

    // Next button
    const nextButton = document.createElement('button');
    nextButton.textContent = 'Next';
    nextButton.disabled = currentPage === totalPages;
    nextButton.addEventListener('click', () => {
        if (currentPage < totalPages) {
            currentPage++;
            displayDresses(currentDressArray);
        }
    });
    paginationContainer.appendChild(nextButton);
}

